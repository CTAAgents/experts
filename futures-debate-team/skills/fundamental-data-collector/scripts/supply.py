def _get_source(symbol: str) -> str:
    """获取数据来源标注"""
    return _SOURCE_REGISTRY.get(
        symbol.upper(),
        "探源自研供需数据库（数据截至2026-07-04）"
    )


def query_supply(symbol: str) -> dict:
    """查询品种供给端数据。

    Args:
        symbol: 品种代码（大小写不敏感）

    Returns:
        dict: 供给数据，含来源标注
    """
    sym = symbol.upper()
    base = _SUPPLY_CACHE.get(sym, {"info": f"无{sym}供给数据"})
    base["_source"] = _get_source(sym)
    base["_updated"] = "2026-07-04"
    return base


def list_available_symbols() -> list:
    """列出有供给数据的品种"""
    return sorted(_SUPPLY_CACHE.keys())
